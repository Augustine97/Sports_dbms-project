    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       String name=txf1.getText();      //insert
        String club=txf4.getText();
        String loc=txf3.getText();
        String sal=txf2.getText();
        String clubid=txf5.getText();
        String rat=txf6.getText();
        

        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn=DriverManager.getConnection("jdbc:mysql://127.0.0.1/sports","root","");
            JOptionPane.showMessageDialog(null,"connected");

            Statement st=conn.createStatement();
            String query="insert into player (pname,salary,location,club,clubid,Rating) values ('"+name+"','"+sal+"','"+loc+"','"+club+"','"+clubid+"','"+rat+"')";
            int p=Integer.parseInt(rat);
           if(p>5){
               
                JOptionPane.showMessageDialog(this, "Invalid rating");
                JOptionPane.showMessageDialog(null,"Enter valid value");
                 
            }
            else{
            st.executeUpdate(query);
            JOptionPane.showMessageDialog(null,"Inserted Successfully");
            }
            
        } catch (HeadlessException | ClassNotFoundException | SQLException e) {
            JOptionPane.showMessageDialog(null,e.getMessage());
        }  
        txf1.setText(" ");
        txf2.setText(" ");
        txf3.setText(" ");
        txf4.setText(" ");
        txf5.setText(" ");
        txf6.setText("");
        
    }//GEN-LAST:event_jButton2ActionPerformed

